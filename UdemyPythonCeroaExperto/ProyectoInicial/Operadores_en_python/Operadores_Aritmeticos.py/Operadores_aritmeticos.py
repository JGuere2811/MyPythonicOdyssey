#operadores Aritmeticos
a = 10
b = 3
#Suma
suma = a + b
print("El resultado de la suma es:", suma)
#Resta
resta = a - b
print("El resultado de la resta es:", resta)
#Multiplicación
multiplicacion = a * b
print("El resultado de la multiplicación es:", multiplicacion)
#División
division = a / b
print("El resultado de la división es:", division)
#División entera
division_entera = a // b
print("El resultado de la división entera es:", division_entera)
#Módulo
modulo = a % b
print("El resultado del módulo es:", modulo)
#Exponente
exponente = a ** b
print("El resultado del exponente es:", exponente)
#Prioridad de los operadores
prioridad = a + b * 2
print("El resultado de la prioridad de los operadores es:", prioridad)
prioridad_con_parentesis = (a + b) * 2
print("El resultado de la prioridad con paréntesis es:", prioridad_con_parentesis)
#Incremento
a += 1  # Equivalente a a = a + 1
print("El valor de a después del incremento es:", a)
#Decremento
a -= 1  # Equivalente a a = a - 1
print("El valor de a después del decremento es:", a)
#Multiplicación y asignación
a *= 2  # Equivalente a a = a * 2
print("El valor de a después de la multiplicación y asignación es:", a)
#División y asignación
a /= 2  # Equivalente a a = a / 2
print("El valor de a después de la división y asignación es:", a)
#Módulo y asignación
a %= 3  # Equivalente a a = a % 3
print("El valor de a después del módulo y asignación es:", a)
#Exponente y asignación
a **= 3  # Equivalente a a = a ** 3
print("El valor de a después del exponente y asignación es:", a)

#División entera y asignación
a //= 2  # Equivalente a a = a // 2
print("El valor de a después de la división entera y asignación es:", a)
