print("*** Operadores de Asignación Compuesta ***")
a, b = 10, 5
print(f"Valor inicial de a: {a} , b: {b}")
# operador +=
a += b  # Equivalente de a = a + b
print(f"Después de a += b, a: {a}")
# operador -=
a -= b  # Equivalente de a = a - b
print(f"Después de a -= b, a: {a}")
# operador *=
a *= b  # Equivalente de a = a * b
print(f"Después de a *= b, a: {a}")
# operador /=
a /= b  # Equivalente de a = a / b
print(f"Después de a /= b, a: {a}")
# operador //=
a //= b  # Equivalente de a = a // b
print(f"Después de a //= b, a: {a}")
# operador %=
a %= b  # Equivalente de a = a % b
print(f"Después de a %= b, a: {a}")
# operador **=
a **= b  # Equivalente de a = a ** b
print(f"Después de a **= b, a: {a}")
