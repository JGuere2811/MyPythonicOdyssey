print("** Operadores de comparacion (Relaciones)**")
a, b = 5, 5
print(f"Valor de a: {a} , b: {b}")
# operador ==
resultado = (a == b)
print(f"El resultado de a == b es: {resultado}")
# operador !=
resultado = (a != b)
print(f"El resultado de a != b es: {resultado}")
# operador >
resultado = (a > b)
print(f"El resultado de a > b es: {resultado}")
# operador <
resultado = (a < b)
print(f"El resultado de a < b es: {resultado}")
# operador >=
resultado = (a >= b)
print(f"El resultado de a >= b es: {resultado}")
# operador <=
resultado = (a <= b)
print(f"El resultado de a <= b es: {resultado}")
# ComparaciÃ³n de cadenas
cadena1 = "Hola"
cadena2 = "Mundo"
resultado = (cadena1 == cadena2)
print(f"El resultado de cadena1 == cadena2 es: {resultado}")
resultado = (cadena1 != cadena2)
print(f"El resultado de cadena1 != cadena2 es: {resultado}")