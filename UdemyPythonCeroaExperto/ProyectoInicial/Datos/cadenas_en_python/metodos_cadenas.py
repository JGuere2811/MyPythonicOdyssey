#Metodos cadena
cadena1 = "Hola Mundo"
print(f"Cadena original: {cadena1}")
#convertir a mayusculas
mayusculas = cadena1.upper()
print(f"Cadena en mayusculas: {mayusculas}")
#convertir a minusculas
minusculas = cadena1.lower()
print(f"Cadena en minusculas: {minusculas}")
#eliminar espacios en blanco al inicio y final
cadena2 = "   Hola Galaxia   "
print(f"Cadena original con espacios: '{cadena2}'")
sin_espacios = cadena2.strip()
print(f"Cadena sin espacios: '{sin_espacios}'")