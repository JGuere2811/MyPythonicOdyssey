#manejo de subcadenas
cadena = "Hola, Mundo!"
#extraer subcadena desde el indice 0 hasta el 4 (no incluye el 4)
subcadena1 = cadena[0:4]
print(f"Subcadena1: {subcadena1}")
print(f"subcadena2: {cadena[6:11]}") #extraer desde el indice 6 hasta el 10 (no incluye el 10)
print(f"subcadena3: {cadena[:5]}") #extraer desde el inicio hasta el indice 4
print(f"subcadena4: {cadena[6:]}") #extraer desde el