#caracteres Especiales en cadenas
print('hola \n mundo')  # Nueva línea
print('hola \t mundo')  # Tabulación
print('Es una cadena con una comilla simple: \' aquí')  # Comilla simple
print("Esta cadena contiene una comilla simple: ' aquí")  # Comilla simple
print("Esta cadena contiene una comilla doble: \" aquí")  # Comilla doble
print("Esta cadena contiene una barra invertida: \\ aquí")  # Barra invertida
