#Manejo de una cadena en Python
cadena1 = 'Hola mundo'
print("cadena1:", cadena1)
#Recuperar el primer caracter de la cadena

primer_caracter = cadena1[0]  # Usando índice negativo para obtener el último carácter
print("Primer caracter de cadena1:", primer_caracter)
#Recuperar el ultimo caracter de la cadena
ultimo_caracter = cadena1[-1]  # Usando índice negativo para obtener el último carácter
print("Ultimo caracter de cadena1:", ultimo_caracter)